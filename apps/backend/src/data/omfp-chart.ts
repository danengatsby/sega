function withUniqueCodes(accounts: SeedAccount[]): SeedAccount[] {
  const unique = new Map<string, SeedAccount>();
  for (const account of accounts) {
    if (unique.has(account.code)) {
      throw new Error(`Cod de cont duplicat in planul OMFP: ${account.code}`);
    }
    unique.set(account.code, account);
  }
  return Array.from(unique.values());
}

function buildAnalyticAccounts(templates: AnalyticTemplate[]): SeedAccount[] {
  const analyticAccounts: SeedAccount[] = [];

  for (const template of templates) {
    const padSize = template.padSize ?? 3;
    for (let index = 1; index <= template.count; index += 1) {
      const suffix = String(index).padStart(padSize, '0');
      analyticAccounts.push({
        code: `${template.baseCode}${suffix}`,
        name: `${template.namePrefix} analitic ${suffix}`,
        type: template.type,
      });
    }
  }

  return analyticAccounts;
}

export const OMFP_ACCOUNTS: SeedAccount[] = withUniqueCodes([
  ...BASE_OMFP_ACCOUNTS,
  ...buildAnalyticAccounts(ANALYTIC_TEMPLATES),
]).sort((left, right) => left.code.localeCompare(right.code, 'ro'));

export const OMFP_ACCOUNTS_COUNT = OMFP_ACCOUNTS.length;

if (OMFP_ACCOUNTS_COUNT < 600) {
  throw new Error(`Planul OMFP incarcat are doar ${OMFP_ACCOUNTS_COUNT} conturi. Sunt necesare minimum 600.`);
}

